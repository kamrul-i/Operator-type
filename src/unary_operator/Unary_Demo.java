package unary_operator;

public class Unary_Demo {
	public static void main(String[] args) {
		
		 
		
		//-------------------------------------------
		int x = 10;
        int result;
        
        result = +x;    //unary plus যেকোন result কে positive হিসেবে দেখায়।।
        System.out.println("Result : "+result);
        
        result = -x;     //unary minus যেকোন result কে negative হিসেবে দেখায়।।
        System.out.println("Result : "+result);
        //-------------------------------------------


        
        //-------------------------------------------
		int y = -10;
        int result1;
        
        result1 = +y;    //unary plus যেকোন result কে positive হিসেবে দেখায়।।
        System.out.println("Result : "+result1);
        
        result1 = -y;     //unary minus যেকোন result কে negative হিসেবে দেখায়।।
        System.out.println("Result : "+result1);
        //--------------------------------------------


        
        

	}

}
