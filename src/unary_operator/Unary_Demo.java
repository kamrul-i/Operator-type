package unary_operator;

public class Unary_Demo {
	public static void main(String[] args) {
		
		
		
		int x = 10;
        int y;

        
        //Increment Demo..................................
        y = ++x;  //x=11
        System.out.println("y = "+y);
        
        y = x;   //x=11
        System.out.println("y = "+y);
               
        
        
        y = x++;  //x=11
        System.out.println("y = "+y);
        
        y = x;    //x= 12
        System.out.println("y = "+y);
        
        
       
	}

}
