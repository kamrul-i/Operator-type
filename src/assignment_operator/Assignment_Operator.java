package assignment_operator;

public class Assignment_Operator {
	public static void main(String[] args) {
		
        int x = 3;
        int y = 2;
        
        x+=y; // x + y = 5
        System.out.println("x = "+x);
        
        x-=y;  //x-y=3
        System.out.println("x = "+x);
        
        x*=y;  //x*y=6
        System.out.println("x = "+x);
        
        x/=y;  //x/y=3
        System.out.println("x = "+x);
        
        x%=y;  //x%y=1
        System.out.println("x = "+x);

		//assignment operator চিহ্ন ( =, +=, -=, *=, /=, %= ) ৬টি।।
	}

}
